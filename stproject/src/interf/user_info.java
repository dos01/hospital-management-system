    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        check();
        try{
           

            //duplicate check
            
           
            String sql1="Select nic from user_info where nic= '" + nic.getText() + "'";
            pst=con.prepareStatement(sql1);
            rs=pst.executeQuery();
            
            if(rs.next()){
                JOptionPane.showMessageDialog( this, "NIC already exists","Error", JOptionPane.ERROR_MESSAGE);
                nic.setText("");
                nic.requestDefaultFocus();
                return;
            }
            //end of duplicate check

            String Name=name.getText();
            String Nic=nic.getText();
            String address=add.getText();
            String con_no=conNo.getText();
            String Email=email.getText();
            String position=pos.getSelectedItem().toString();
            String Uname=uname.getText();
            String Gender=gender.getSelectedItem().toString();
            String Pwd=pwd.getText();

            String q="INSERT INTO user_info(name,position,address,con_no,gender,NIC,email,username,password) values(?,?,?,?,?,?,?,?,?)";
            pst=con.prepareStatement(q);
            pst.setString(1, Name);
            pst.setString(2, position);
            pst.setString(3, address);
            pst.setString(4, con_no);
            pst.setString(5, Gender);
            pst.setString(6, Nic);
            pst.setString(7, Email);
            pst.setString(8, Uname);
            pst.setString(9, Pwd);
            pst.execute();
            
            JOptionPane.showMessageDialog(this,"Successfully Inserted");

            //load table
            tableload();
            reset();
        }
        catch(Exception e)
        {
            System.out.println("Error in inserting");
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_SubmitActionPerformed

