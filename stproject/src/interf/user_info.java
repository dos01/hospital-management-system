    public void check(){
            if(name.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Enter NIC");
                return;
            }

            if (pos.getSelectedItem().equals("-Select-")) {
                JOptionPane.showMessageDialog( this, "Enter name");
                return;
            }
            if (add.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Enter address");
                return;
            }
            if (conNo.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter contact no.");
                return;
            }

            if (nic.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Enter qualifications");
                return;
            }
            if (email.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Enter specialization");
                return;
            }
            if (gender.getSelectedItem().equals("-Select-")) {
                JOptionPane.showMessageDialog( this, "Select gender");
                return;
            }
            if (pwd.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Enter date");
                return;
            }
            if (uname.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Enter date");
                return;
            }
             //validate nic
           if(!(nic.getText().trim().matches("^[0-9]{9}[vVxX]$")))
            {
             JOptionPane.showMessageDialog(null,"Invalid NIC ");
             return;
            }
    }
    
