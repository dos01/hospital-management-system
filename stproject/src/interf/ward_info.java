     public void tableload(){
           try{
            String sql="select * from ward_info";
            pst=con.prepareStatement(sql);
            rst=pst.executeQuery();
            
            jTable1.setModel(DbUtils.resultSetToTableModel(rst));
        }
        catch (Exception e){
            System.out.println("load error");
        }
       }
     public void checkempty(){
        if (bno.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Bed ID","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        if (wardn.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please Select Ward Name","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        if (Price.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Price","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        if ((dname.getSelectedItem().toString()).equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Price","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
       }
     
     public void clear(){
         bno.setText("");
         wardn.setText("");
         dname.setSelectedIndex(0);
         Price.setText("");
     }
     
     public void adddoctor(){
         try {
             String que="SELECT dname from doctor";
             pst=con.prepareStatement(que);
             rst=pst.executeQuery();
             
             while(rst.next()){
                 dname.addItem(rst.getString("dname"));
             }
         } catch (Exception e) {
         }
     }
