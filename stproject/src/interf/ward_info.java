    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        checkempty();
        String bn=bno.getText();
        String wardname=wardn.getText();
        String doc=dname.getSelectedItem().toString();
        String price=Price.getText();
        String s="Available";
        try {

            String que1="INSERT ward_info (bedno,ward_name,doctor,status,price) values (?,?,?,?,?)";
            pst=con.prepareStatement(que1);
            pst.setString(1,bn);
            pst.setString(2,wardname);
            pst.setString(3,doc);
            pst.setString(4,s);
            pst.setString(5,price);
            pst.execute();

            JOptionPane.showMessageDialog(null,"Successfully Inserted");
            tableload();
            clear();
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton3ActionPerformed

